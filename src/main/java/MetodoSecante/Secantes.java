    /**
     * Este método se utiliza para validar que el texto obtenido de un JTextField sea un número (entero o decimal).
     * No debe estar vacío y no debe contener letras ni signos (excepto el punto decimal).
     *
     * @param txtField El JTextField que se va a validar.
     * @return true si el texto es un número, false en caso contrario.
     */
    public boolean validarTxtField(javax.swing.JTextField txtField) {
        // Validar que no esté vacío y que sea un número (entero o decimal)
        if (txtField.getText().isEmpty() || !txtField.getText().matches("-?[0-9]+\\.?[0-9]*")) {
            // Pinta el borde de rojo si no es válido
            txtField.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            return false;
        } else {
            // Pinta el borde de negro si es válido
            txtField.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0)));
            return true;
        }
    }
    /**
     * Este método se utiliza para validar que el texto obtenido de un JTextField sea un entero.
     *
     * @param txtfield El JTextField que se va a validar.
     * @return true si el texto es un entero, false en caso contrario.
     */
    public boolean Esint(javax.swing.JTextField txtfield){
        try {
            Integer.parseInt(txtfield.getText());
            //pinta el borde de negro
            txtfield.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0,0,0)));
            return true;
        } catch (NumberFormatException e) {
            //pinta el borde de rojo
            txtfield.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255,0,0)));
            return false;
        }
    }



