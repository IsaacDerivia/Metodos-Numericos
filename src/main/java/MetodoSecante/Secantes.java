    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        //crear variables para los txtfield
        double x6, x5, x4, x3, x2, x, cte, xi, ib, errorAceptado;
        int decimales;
        //validar cada campo de texto con su respectivo metodo
        if (validarTxtField(txtX6) && validarTxtField(txtx5) && validarTxtField(txtx4) && validarTxtField(txtx3) && validarTxtField(txtx2) && validarTxtField(txtx) && validarTxtField(txtConstante) && validarTxtField(txtxia) && validarTxtField(txtib) && validarTxtField(txtErrorAceptado) && validarTxtField(txtDeciamles) && Esint(txtDeciamles)) {
            //asignar el valor de cada txtfield a su respectiva variable
            x6 = Double.parseDouble(txtX6.getText());
            x5 = Double.parseDouble(txtx5.getText());
            x4 = Double.parseDouble(txtx4.getText());
            x3 = Double.parseDouble(txtx3.getText());
            x2 = Double.parseDouble(txtx2.getText());
            x = Double.parseDouble(txtx.getText());
            cte = Double.parseDouble(txtConstante.getText());
            xi = Double.parseDouble(txtxia.getText());
            ib = Double.parseDouble(txtib.getText());
            errorAceptado = Double.parseDouble(txtErrorAceptado.getText());
            decimales = Integer.parseInt(txtDeciamles.getText());
            //crear un objeto de la clase secantes
            Secante secante = new Secante(x6, x5, x4, x3, x2, x, cte, xi, ib, errorAceptado, decimales);
            //crear un objeto de la clase DefaultTableModel
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            //limpiar la tabla
            modelo.setRowCount(0);
            //llamar al metodo calcular de la clase secantes
            secante.calcular(modelo);
        } else {
            //mensaje de error
            javax.swing.JOptionPane.showMessageDialog(this, "Corriga los campos en rojo");
        }

    }//GEN-LAST:event_btnCalcularActionPerformed

